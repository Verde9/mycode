#!/usr/bin/env python3

# list containing different snakes
snakes = ["cobra", "python", "black snake"]

# This will print snakes
print(snakes)

# This will extend the given value "viper" to the list snakes
snakes.extend("viper")
print(snakes)

# This will append the given value "viper" to the list snakes
snakes.append("viper")
print(snakes)

# This will remove the last item in the list snakes
snakes.pop()
print(snakes)
